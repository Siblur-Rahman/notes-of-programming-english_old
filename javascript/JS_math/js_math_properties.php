<h2>Math Properties (Constants)</h2>
      The syntax for any Math property is : Math.property. <br>

      JavaScript provides 8 mathematical constants that can be accessed as Math properties:
      <div class="code_p">
      Math.E        // returns Euler's number <br>
      Math.PI       // returns PI <br>
      Math.SQRT2    // returns the square root of 2 <br>
      Math.SQRT1_2  // returns the square root of 1/2 <br>
      Math.LN2      // returns the natural logarithm of 2 <br>
      Math.LN10     // returns the natural logarithm of 10 <br>
      Math.LOG2E    // returns base 2 logarithm of E <br>
      Math.LOG10E   // returns base 10 logarithm of E <br>

      

