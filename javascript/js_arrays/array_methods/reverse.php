   <h2>array.reverse()<span class="itc"><a href=""> Details</a></span></h2>
      
      <div class="mark">
      The <spa class="itc">reverse()</spa> method reverses the order of the elements in an array. <br>
      The reverse() method overwrites the original array.
      </div>
      <h2>Syntax</h2>
      <div class="code_p">
      array.reverse()
      </div>
      <h2>Example</h2>
      <div class="code_p">
      let nambers = [0, 1, 2, 3,4, 5] <br>

      let myNumbers = nambers.reverse() <br>

      console.log(myNumbers)//[ 5, 4, 3, 2, 1, 0 ]
