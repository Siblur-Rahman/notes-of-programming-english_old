   <div class="mark">
   map() creates a new array from calling a function for every array element.  <br><br>

   map() does not execute the function for empty elements.  <br><br>

   map() does not change the original array. 
   </div>
   <h2>Syntax</h2>
   <div class="code_p">
      array.map(function(currentValue, index, arr), thisValue)
