      <h2>Array splice()</h2>
      <div class="mark">
      The splice() method adds and/or removes array elements. <br>
       <br>
      The splice() method overwrites the original array.
      </div>
      <h2>Syntax</h2>
      <div class="code_p">
      array.splice(index, howmany, item1, ....., itemX)
      </div>
      <h2>Example</h2>
      <div class="code_p">
      const numbers =[1, 2, 3, 4, 5, 6, 7, 8]; <br>
      const part = numbers.slice(3, 8) <br>
      console.log(part);
      </div>
      <div class="output">
         <h2>output</h2>
         [ 4, 5, 6, 7, 8 ]
