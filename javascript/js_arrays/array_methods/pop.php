   <h2>Array.pop()<span class="itc"><a href="https://www.w3schools.com/jsref/jsref_pop.asp" target="_blank"> Details</a></span></h2>
      <h2></h2>
      <div class="mark">
         The pop() method removes (pops) the last element of an array. <br>

      The pop() method changes the original array. <br>

      The pop() method returns the removed element.
      </div>
      <h2>Syntax</h2>
      <div class="code_p">
      array.pop()
      </div>
      <h2>removes (pop) the last element of an array</h2>
      <div class="code_p">
         const numbers = [45, 4, 9, 16, 25];

         numbers.pop()
         console.log(numbers)//[ 45, 4, 9, 16 ]
      </div>
      <h2>returns the removed element.</h2>
      <div class="code_p">
         const numbers = [45, 4, 9, 16, 25];

         let lastElement =numbers.pop()
         console.log(lastElement)//25
      </div>
