<p class="accordion">value</p>
<div class="panel">
    <h2>Input Text value Property <span class="itc"><a href="https://www.w3schools.com/jsref/prop_text_value.asp"  target="_blank">Details</a></span></h2>
    <div class="mark">
        The value property sets or returns the value of the value attribute of a text field. <br>

        The value property contains the default value OR the value a user types in (or a value set by a script).
    </div>
    <h2>Syntax</h2>
    <p>Return the value property:</p>
    <div class="code_p">
        textObject.value
    </div>
    <p>Set the value property:</p>
    <div class="code_p">
        textObject.value = text
    </div>
