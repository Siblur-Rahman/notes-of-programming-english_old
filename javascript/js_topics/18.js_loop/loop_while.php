The while loop loops through a block of code as long as a specified condition is true.

     <h3>Syntax</h3>
     <pre class="code_p">
     while (condition) {
      // code block to be executed
      }
     </pre>
     <h2>Example</h2>
     <pre>
     let x = "";
      let i = 1;
      while(i <=10){
         text = "This is "+i;
         console.log(text);
         i++
      }
      </pre>
