   <div class="mark">
      The replaceAll() method searches a string for a value or a regular expression. <br>

      The replaceAll() method returns a new string with all values replaced. <br>

      The replaceAll() method does not change the original string. <br>

      The replaceAll() method was introduced in JavaScript 2021. <br>

      The replaceAll() method does not work in Internet Explorer.
   </div>
   <h2>Syntax</h2>
   <div class="code_p">
      string.replaceAll(searchValue, newValue)
   </div>
