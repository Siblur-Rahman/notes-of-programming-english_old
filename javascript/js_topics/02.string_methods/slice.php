   <h2>String slice()<span class="itc"><a href="https://www.w3schools.com/jsref/jsref_slice_string.asp"> Details</a></span></h2>
   <div class="code_p">
      <span class="itc">slice()</span> extracts a part of a string and returns the extracted part in a new string. <br>

      The method takes 2 parameters: start position, and end position (end not included).
      <h2>Example</h2>
      <div class="code_p">
      var text ="0123456789"; <br>

      console.log(text.slice(2, 5));//234
