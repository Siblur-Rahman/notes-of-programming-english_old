   <h2>JavaScript Method <span class="itc"><a href="https://www.w3schools.com/js/js_object_methods.asp" target="_blank"> Details</a></span></h2>
   <div class="mark">
   JavaScript methods are actions that can be performed on objects.

   A JavaScript method is a property containing a function definition.
   </div>
   <h2>example</h2>
   <pre class="code_p">
   const person = {
    firstName : "Siblur",
    lastName  : "Rahman",
    id        : 691048,
    fullName  : function(){
        return this.firstName + " " + this.lastName;
    }
   }
   </pre>
   <div class="mark">Methods are functions stored as object properties.</div>
