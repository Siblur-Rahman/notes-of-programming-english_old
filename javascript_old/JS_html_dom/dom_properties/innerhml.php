<h2>HTML DOM Element innerHTML <span class="itc"><a href="https://www.w3schools.com/jsref/prop_html_innerhtml.asp"  target="_blank">Details</a></span></h2>
<p>The innerHTML property sets or returns the HTML content (inner HTML) of an element.</p>
<h4>Get the HTML content of an element with id="demo":</h4>
<div class="code_p">
    let content = "I love my Country" <br>
    document.getElementById("demo1").innerHTML = content;
</div>
<h4>Change the HTML content of an element with id="demo":</h4>
<div class="code_p">document.getElementById("demo").innerHTML = "I have changed!";</div>
