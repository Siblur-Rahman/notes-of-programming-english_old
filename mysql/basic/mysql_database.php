      <h3 class="all-center100"></h3>
   <pre class="s_code">
   Syntax
      CREATE TABLE table_name (
        column1 datatype,
        column2 datatype,
        column3 datatype,
      ....
    ); 
    <span class="mark">Example</span>
    CREATE TABLE student(
      Roll int(5) PRIMARY KEY AUTO_INCREMENT,
      Name varchar(20),
      Gender varchar(5),
      GPA float(3,2),
      City varchar(20)  
   );
   </pre>
 
        
