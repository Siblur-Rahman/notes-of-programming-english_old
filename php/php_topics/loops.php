   <h1>PHP Loop</h1>
      <h4>
      Often when you write code, you want the same block of code to run over and over again a certain number of times. So, instead of adding several almost equal code-lines in a script, we can use loops.<br><br>

      Loops are used to execute the same block of code again and again, as long as a certain condition is true. <br><br>
      In PHP, we have the following loop types: <br> <br>

            <span class="text-danger">while</span> - loops through a block of code as long as the specified condition is true <br>
            <span class="text-danger">do...while</span> - loops through a block of code once, and then repeats the loop as long as the specified condition is true <br>
            <span class="text-danger">for</span> - loops through a block of code a specified number of times <br>
            <span class="text-danger">foreach</span> - loops through a block of code for each element in an array <br>
            <span class="mark">The following chapters will explain and give examples of each loop type.</span>
      </h4>
