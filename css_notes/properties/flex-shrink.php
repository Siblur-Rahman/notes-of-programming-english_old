<div class="panel">
    <h2>CSS flex-shrink<span><a href="https://www.w3schools.com/cssref/css3_pr_flex-shrink.php" target="_blank"> Details</a></span></h2>
    <div class="mark">
    The  <span class="itc">flex-shrink</span> property specifies how the item will shrink relative to the rest of the flexible items inside the same container.
        <br><br>
    Note: If the element is not a flexible item, the flex-shrink property has no effect.
    </div>
    <h2>Syntax</h2>
    <div class="code_p">flex-shrink: number|initial|inherit;</div>
