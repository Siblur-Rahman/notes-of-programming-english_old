<h2>CSS flex property<span><a href="" target="_blank">Details</a></span></h2>
    The flex property is a shorthand property for:

    <li>flex-grow</li>
    <li>flex-shrink</li>
    <li>flex-basis</li>
    The flex property sets the flexible length on flexible items.
        <br><br>
    Note: If the element is not a flexible item, the flex property has no effect.
    <h2>Syntax</h2>
    <div class="code_p">flex: flex-grow flex-shrink flex-basis|auto|initial|inherit;</div>
