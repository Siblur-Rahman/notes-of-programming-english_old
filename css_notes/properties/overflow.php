    <h2>CSS overflow Property<span><a href="https://www.w3schools.com/cssref/pr_pos_overflow.php" target="_blank"> Details</a></span></h2>
    <div>
        The overflow property specifies what should happen if content overflows an element's box.

        This property specifies whether to clip content or to add scrollbars when an element's content is too big to fit in a specified area.

        Note: The overflow property only works for block elements with a specified height.
    </div>
    <div class="mark">Note: The overflow property only works for block elements with a specified height.</div>
    <h2>CSS Syntax</h2>
    <div class="code_p">overflow: visible|hidden|clip|scroll|auto|initial|inherit;</div>
