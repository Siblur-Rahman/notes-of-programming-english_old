    <h2>Syntax</h2>
    <div class="mark">
    The <span class="itc">align-content</span> property specifies how flex lines are distributed along the cross axis in a flexbox container.

    In flexbox layout, the main axis is in the <span class="itc">flex-direction</span> (default is 'row', horizontal), and the cross axis is perpendicular to the main axis (default is 'column', vertical). <br><br>

    Tip: Use the justify-content property to align the items on the main axis (horizontally). <br><br>

    Note: The <span class="itc">align-content</span> property can also be used on a grid container to align grid items in the block direction. For pages in English, block direction is downward and inline direction is left to right.
