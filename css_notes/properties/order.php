    The <span class="itc">order</span> property specifies the order of a flexible item relative to the rest of the flexible items inside the same container.
    <br><br>
    Note: If the element is not a flexible item, the order property has no effect.
    </div>
    <h2>Syntax</h2>
    <div class="code_p">order: number|initial|inherit;</div>
