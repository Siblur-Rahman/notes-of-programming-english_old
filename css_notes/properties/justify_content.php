<div class="panel">
    <h2>CSS justify-content Property<span><a href="https://www.w3schools.com/cssref/css3_pr_justify-content.php" target="_blank">Details</a></span></h2>
        The justify-content property aligns the flexible container's items when the items do not use all available space on the main-axis (horizontally).

    <div class="mark">Tip: Use the align-items property to align the items vertically.</div>
    <div class="mark">
        Note: The justify-content property can also be used on a grid container to align grid items in the inline direction. For pages in English, inline direction is left to right and block direction is downward.
    </div>
    <h2>Syntax</h2>
    <div class="code_p">justify-content: flex-start | flex-end | center | space-between | space-around | space-evenly | initial | inherit;</div>
</div>