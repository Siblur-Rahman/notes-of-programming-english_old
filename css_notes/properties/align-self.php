<div class="panel">
    <h2>CSS align-self<span><a href="https://www.w3schools.com/cssref/css3_pr_align-self.php" target="_blank"> Details</a></span></h2>
    <div class="mark">
    The <span class="itc">align-self</span> property specifies the alignment in the block direction for the selected item inside a flexbox or grid container.
        <br>
    For pages in English, block direction is downward and inline direction is left to right.
        <br>
    Tip: To align a grid item in the inline direction instead of the block direction, use <span class="itc">justify-self</span> or <span class="itc">justify-items</span> properties.
        <br>
    Note: The align-self property overrides the grid or flexible container's <span class="itc">align-items</span> property.
    </div>
    <h2>Syntax</h2>
    <div class="code_p">align-self: auto|stretch|center|flex-start|flex-end|baseline|initial|inherit;</div>
</div>
    
