    <div class="mark">
    The gap property defines the size of the gap between the rows and between the columns in flexbox, grid or multi-column layout. It is a shorthand for the following properties:

    <li>row-gap</li>
    <li>column-gap</li>
    Note: The gap property was formerly known as <a href="../css_properties/css_properties.php" class="span itc" target="_blank">grid-gap</a>.
    </div>
    <h2>Syntax</h2>
