    <h2>CSS grid-template-rows<span><a href="https://www.w3schools.com/cssref/pr_grid-column.php" target="_blank"> Details</a></span></h2>
    <div class="mark">
    The grid-template-rows property specifies the number (and the heights) of the rows in a grid layout.
        <br><br>
    The values are a space-separated list, where each value specifies the height of the respective row.
    </div>
    <h2>Syntax</h2>
    <div class="code_p">grid-template-rows: none|auto|max-content|min-content|length|initial|inherit;</div>
